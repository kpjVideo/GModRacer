function ValidName( _Name )
	local name = string.lower( _Name )

	for i = 1, string.len( name ) do
		local validLetter = false
		local curChar = string.sub(name, i, i)

		for k, v in pairs( GAMEMODE.AllowedCharacters ) do
			if (curChar == v) then
				validLetter = true
				break
			end
		end

		if (not validLetter) then return false end
	end

	return true
end